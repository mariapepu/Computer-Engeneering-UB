    /**
     * @param args the command line arguments
     */
    public void omplirLlistaFitxers() {

        DefaultListModel model = new DefaultListModel();
        model.clear();

        for (String item : c.mostrarBiblioteca()) {
            model.addElement(item);
        }
        lstFitxers.setModel(model);

    }


