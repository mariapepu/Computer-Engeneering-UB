    private void btnAfegirFitxerAlbumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAfegirFitxerAlbumActionPerformed
        if (lstFitxers.getSelectedIndex() != -1) {
            try {
                c.afegirFitxer(nom, lstFitxers.getSelectedIndex());
            } catch (AplicacioException e) {
                JOptionPane j = new JOptionPane();
                j.showMessageDialog(this, e.getMessage(), "Error", j.ERROR_MESSAGE);
            }
        }
        else{
            JOptionPane j = new JOptionPane();
            j.showMessageDialog(this, "Selecciona un fitxer", "Error", j.ERROR_MESSAGE);
        }
        x.omplirLlistaFitxers();
        dispose();
    }//GEN-LAST:event_btnAfegirFitxerAlbumActionPerformed

